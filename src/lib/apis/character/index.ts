import { WEBUI_API_BASE_URL } from '$lib/constants';

export const createNewCharacter = async (
	token: string,
	title: string,
	systemPrompt: string,
) => {
	let error = null;

	const res = await fetch(`${WEBUI_API_BASE_URL}/character/create`, {
		method: 'POST',
		headers: {
			Accept: 'application/json',
			'Content-Type': 'application/json',
			authorization: `Bearer ${token}`
		},
		body: JSON.stringify({
			title: title,
			system_prompt: systemPrompt,
		})
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			error = err.detail;
			console.log(err);
			return null;
		});

	if (error) {
		throw error;
	}

	return res;
};

